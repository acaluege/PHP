Encapsulamento:

Nota1:
    A palavra Encapsulamento podemos associa-la como sendo uma "capsula"
    como por exemplo as capsulas dos remédios que tomamos, ou as capsula
    dos astronautas que vão para a lua.

Nota2:
    As capsulas tem a missão de manter as coisas unidas ou seja juntas.
    Assim como as "classes" que também são capazes de manter as coisas
    unidas.

    Ex1: Podemos ter +10 funções dentro de uma "classe" o que significa 
    que as "funções" estão encapsuladas dentro da "classe" pois ela mantem 
    junta as funções.
    

    




